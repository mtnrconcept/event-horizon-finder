const dictionaries: Record<AppLocale, Dictionary> = { fr, en, pl, it, ru, es };
const localeTags: Record<AppLocale, string> = {
  fr: "fr-CH",
  en: "en-GB",
  pl: "pl-PL",
  it: "it-IT",
  ru: "ru-RU",
  es: "es-ES",
};
const STORAGE_KEY = "global-party.locale";

function resolveLocale(value?: string | null): AppLocale | null {
  const code = value?.trim().toLowerCase().split(/[-_]/)[0];
  return SUPPORTED_LOCALES.find((locale) => locale === code) ?? null;
}

type TranslationContextValue = {
  locale: AppLocale;
  localeTag: string;
  setLocale: (locale: AppLocale) => void;
  t: (key: TranslationKey, variables?: Record<string, string | number>) => string;
  formatNumber: (value: number) => string;
  tr: (phrase: string, variables?: Record<string, string | number>) => string;
  categoryLabel: (slug: string, fallback?: string) => string;
  genreLabel: (slug: string, fallback?: string) => string;
};

const TranslationContext = createContext<TranslationContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AppLocale>("fr");

  const setLocale = useCallback((nextLocale: AppLocale) => {
    setLocaleState(nextLocale);
    document.documentElement.lang = nextLocale;
    try {
      window.localStorage.setItem(STORAGE_KEY, nextLocale);
    } catch {
      // The selected language remains active for the current page.
    }
  }, []);

  useEffect(() => {
    let preferred: AppLocale | null = null;
    try {
      preferred = resolveLocale(window.localStorage.getItem(STORAGE_KEY));
    } catch {
      // Fall back to browser language detection.
    }
    preferred ??= window.navigator.languages.map(resolveLocale).find(Boolean) ?? null;
    setLocale(preferred ?? "fr");
  }, [setLocale]);

  const t = useCallback(
    (key: TranslationKey, variables: Record<string, string | number> = {}) =>
      Object.entries(variables).reduce(
        (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
        dictionaries[locale][key],
      ),
    [locale],
  );
  const formatNumber = useCallback(
    (value: number) => new Intl.NumberFormat(localeTags[locale]).format(value),
    [locale],
  );
  const tr = useCallback(
    (phrase: string, variables: Record<string, string | number> = {}) => {
      if (Object.prototype.hasOwnProperty.call(UI_TRANSLATIONS, phrase)) {
        return translateUiPhrase(locale, phrase as UiTranslationPhrase, variables);
      }
      return Object.entries(variables).reduce(
        (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
        phrase,
      );
    },
    [locale],
  );
  const categoryLabel = useCallback(
    (slug: string, fallback?: string) => translateCategory(locale, slug, fallback),
    [locale],
  );
  const genreLabel = useCallback(
    (slug: string, fallback?: string) => translateGenre(locale, slug, fallback),
    [locale],
  );

  const value = useMemo(
    () => ({
      locale,
      localeTag: localeTags[locale],
      setLocale,
      t,
      tr,
      categoryLabel,
      genreLabel,
      formatNumber,
    }),
    [categoryLabel, formatNumber, genreLabel, locale, setLocale, t, tr],
  );

  return <TranslationContext.Provider value={value}>{children}</TranslationContext.Provider>;
}

export function useTranslation() {
  const context = useContext(TranslationContext);
  if (!context) throw new Error("useTranslation must be used within LanguageProvider");
  return context;
}
