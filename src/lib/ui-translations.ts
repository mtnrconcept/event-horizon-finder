export function translateUiPhrase(
  locale: AppLocale,
  phrase: UiTranslationPhrase,
  variables: Record<string, string | number> = {},
) {
  const message: string = locale === "fr" ? phrase : UI_TRANSLATIONS[phrase][locale];
  return Object.entries(variables).reduce<string>(
    (translated, [name, value]) => translated.replaceAll(`{${name}}`, String(value)),
    message,
  );
}
